class Weather {
  String _description ;

  String get description => _description;

  set description(String description) {
    _description = description;
  }


  String _conditionImage;

  String get conditionImage => _conditionImage;

  set conditionImage(String conditionImage) {
    _conditionImage = conditionImage;
  }


  String _temperature;

  String get temperature => _temperature;

  set temperature(String temperature) {
    _temperature = temperature;
  }
  String _wind;

  String get wind => _wind;

  set wind(String wind) {
    _wind = wind;
  }
  String _humid;

  String get humid => _humid;

  set humid(String humid) {
    _humid = humid;
  }
  String _locationName;

  String get locationName => _locationName;

  set locationName(String locationName) {
    _locationName = locationName;
  }

}
